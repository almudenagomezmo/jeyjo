const BRAND_LABELS: Partial<Record<PaymentMethodCode, string>> = {
  card: "Visa y Mastercard",
  bizum: "Bizum",
  paypal: "PayPal",
  apple_pay: "Apple Pay",
  google_pay: "Google Pay",
  transfer: "Transferencia bancaria",
};

export function PaymentMethodBrandIcon({ code, className }: PaymentMethodBrandIconProps) {
  const label = BRAND_LABELS[code as PaymentMethodCode] ?? code;

  switch (code) {
    case "card":
      return (
        <IconBadge className={cn("gap-1.5", className)} wide aria-label={label}>
          <VisaLogo />
          <MastercardLogo />
        </IconBadge>
      );
    case "bizum":
      return (
        <IconBadge className={className} wide aria-label={label}>
          <BizumLogo />
        </IconBadge>
      );
    case "paypal":
      return (
        <IconBadge className={className} wide aria-label={label}>
          <PayPalLogo />
        </IconBadge>
      );
    case "apple_pay":
      return (
        <IconBadge className={className} wide aria-label={label}>
          <ApplePayLogo />
        </IconBadge>
      );
    case "google_pay":
      return (
        <IconBadge className={className} wide aria-label={label}>
          <GooglePayLogo />
        </IconBadge>
      );
    case "transfer":
      return (
        <IconBadge className={className} aria-label={label}>
          <TransferLogo />
        </IconBadge>
      );
    default:
      return null;
  }
}
