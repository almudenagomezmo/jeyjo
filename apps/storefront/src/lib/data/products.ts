export function getProduct(id: string): Product | undefined {
  return PRODUCTS.find((p) => p.id === id);
}

export function getProductsByCategory(categoryId: string, subcategoryId?: string): Product[] {
  return PRODUCTS.filter(
    (p) => p.categoryId === categoryId && (!subcategoryId || p.subcategoryId === subcategoryId),
  );
}

export function getBestsellers(limit = 6): Product[] {
  return PRODUCTS.filter((p) => p.bestseller).slice(0, limit);
}

export function getEcoProducts(limit = 4): Product[] {
  return PRODUCTS.filter((p) => p.eco).slice(0, limit);
}

/** Naive cross-sell: other products in the same category. */
export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return PRODUCTS.filter((p) => p.categoryId === product.categoryId && p.id !== product.id).slice(
    0,
    limit,
  );
}
